#Transformação

frase = 'Curso em Vídeo Python'

#Método replace troca a o primeiro parametro encontrado na string pelo segundo
print('Original era: {}'.format(frase))
print('Agora é: {}'.format(frase.replace('Python','Android')))
print('\n')





#Função Upper transforma tudo em maiusculo
print('Original era: {}'.format(frase))
print('Agora é: {}'.format(frase.upper()))
print('\n')




#Função Lower transforma tudo em minúsculo
print('Original era: {}'.format(frase))
print('Agora é: {}'.format(frase.lower()))
print('\n')


#Função capitalize deixa a primeira letra da frase toda em maiuscula
print('Original era: {}'.format(frase))
print('Agora é: {}'.format(frase.capitalize()))
print('\n')

#Função title deixa a primeira letra de cada frase em maiuscula
print('Original era: {}'.format(frase))
print('Agora é: {}'.format(frase.title()))
print('\n')


