frase = "Curso em Vídeo Python"

#[Começa me 9 e vai ate a posição 13]
print(frase[9:14])

print('\n')

#[Começa de : Vai até : pulando de]
print(frase[:21:2])

print('\n')

#[Começa do 0:vai até o 14]
print(frase[:14]) 

print('\n')

#Começa em [9: até o final: de 3 em 3] Fatiamento
print(frase[9::3])



